"""
Leia um valor de comprimento em polegadas (P) e apresente-o convertido em centímetros (C).
A fórmula de conversão é:
 C = P * 2.54

"""

# Enter


polegadas = int(input("Informe o valor do comprimento em polegadas: "))

# Processing


c = polegadas * 2.54

# Exit


print(f'{polegadas} polegadas equivale a {c} centímetros.')
