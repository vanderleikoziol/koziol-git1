"""
Leia a temperatura em graus Celsius (C) e apresente-a convertida em graus Kelvin (K).
A fórmula de conversão é:
K = C + 273.15

"""

# Enter


c = int(input('Informe a temperatura em graus Celsius: '))

# Processing


k = c + 273.15

# Exit


print(f'{c} graus Celsius equivale a {k} graus Kelvin')
